package com.mayab.patrones.proxy;

/**
 *
 * @author galindo
 */
public class Usuario {
    
    private String name;
    private int type;
    
    public Usuario(String n, int t){
        this.name = n;
        this.type = t;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
    
}
